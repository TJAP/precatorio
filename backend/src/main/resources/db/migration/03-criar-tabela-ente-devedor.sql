CREATE TABLE ente_devedor (
    id BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    ente VARCHAR(150) NOT NULL,
    cnpj NUMERIC(14,0) NOT NULL,
    limite_prioridade NUMERIC(15,2) NOT NULL,
    com_vinculo VARCHAR(100),
    sem_vinculo VARCHAR(100),
    conta VARCHAR(20),
    acordo VARCHAR(20)
);

